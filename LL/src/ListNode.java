
public class ListNode {

    /** The data. */
    int data;
    
    /** The next. */
    ListNode next;
    
    /**
     * Gets the data.
     *
     * @return the data
     */
    public int getData() {
        return data;
    }
    
    /**
     * Sets the data.
     *
     * @param data the new data
     */
    public void setData(int data) {
        this.data = data;
    }
    
    /**
     * Gets the next.
     *
     * @return the next
     */
    public ListNode getNext() {
        return next;
    }
    
    /**
     * Sets the next.
     *
     * @param next the new next
     */
    public void setNext(ListNode next) {
        this.next = next;
    }
    
    
}
